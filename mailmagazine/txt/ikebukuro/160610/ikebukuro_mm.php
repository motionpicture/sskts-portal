6/18（土）公開<br>
『貞子vs伽椰子』<br><br>

見たら2日後に必ず死ぬという、“呪いのビデオ”を手にした女子大生、有里。ビデオの不気味な映像を見てしまった親友、夏美を救うべく、都市伝説研究家でもある大学教授・森繁に助けを求める。しかし悪霊祓いの最中に、おぞましい惨劇が勃発してしまう。一方、“呪いの家”の向かいに転居してきた鈴花。ある夜、行方不明になった小学生の姿を目撃し、呪いの家へ向かう。おそるおそる内部に足を踏み入れた鈴花と両親に、この家に棲みつく伽梛子と俊雄が襲いかかるのだった。2つの呪いを解くために、霊媒師・経蔵はある秘策を企てる。それは貞子と伽梛子を激突させ、同時消滅させる計画だった。<br><br>

