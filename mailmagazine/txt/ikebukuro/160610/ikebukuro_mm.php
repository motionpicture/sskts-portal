『MARS（マース）〜ただ、君を愛してる〜』<br><br>

「MARS（マース）」―それは戦いの神の名。愛をかけて運命と闘う、ピュアで残酷なラブストーリー。海で奇跡的に出逢った零とキラ。過去に心の傷を抱えながら孤独に生きてきた２人は惹かれあい、恋に落ちる。そこに、零の死んだ弟・聖の親友、牧生が現れる。よき理解者であるように見えた牧生は、実は零に特別な感情を抱き執着、キラの忌わしい過去を突き止め、２人を引き裂こうとする。豹変した牧生の狂おしいほど切ない想いは、純粋すぎるがゆえに残酷な愛へと変わり、零とキラの運命に衝撃的な結末を引き寄せようとしていた─。<br><br>


監督：耶雲哉治<br>
